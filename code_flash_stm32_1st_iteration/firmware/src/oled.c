/*
 * SSD1306 128x64 OLED driver over I2C1.
 *
 * Text-only. A 1 KB framebuffer holds the current screen contents; putc()
 * draws into it, advances the cursor, scrolls when needed, then flushes
 * only the affected page(s) so writes appear one character at a time
 * (typewriter effect) without paying the full-frame cost every char.
 *
 * Font: classic 5x7 bitmap (public-domain, same table used in Adafruit
 * SSD1306, u8g2, sparkfun-libraries, etc.). Each glyph is 5 columns of
 * 8 bits (bit0 top). We append a 1-pixel spacer to get 6x8 cells.
 */

#include "oled.h"

#include <string.h>
#include <stdint.h>

#include "i2c.h"

#define OLED_ADDR   0x3C
#define OLED_W      128
#define OLED_H      64
#define OLED_PAGES  (OLED_H / 8)
#define CHAR_W      6
#define CHAR_H      8
#define COLS        (OLED_W / CHAR_W)   /* 21 */
#define ROWS        (OLED_H / CHAR_H)   /* 8  */

static uint8_t fb[OLED_W * OLED_PAGES];
static int cursor_row;
static int cursor_col;

