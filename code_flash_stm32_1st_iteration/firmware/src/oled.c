static const uint8_t OLED_INIT_SEQ[] = {
    0xAE,        /* display off                    */
    0xD5, 0x80,  /* clock div / oscillator freq    */
    0xA8, 0x3F,  /* multiplex ratio (0x1F for x32) */
    0xD3, 0x00,  /* display offset                 */
    0x40,        /* start line = 0                 */
    0x8D, 0x14,  /* charge pump on                 */
    0x20, 0x00,  /* horizontal addressing mode     */
    0xA1,        /* segment remap                  */
    0xC8,        /* COM output scan direction      */
    0xDA, 0x12,  /* COM pins config (0x02 for x32) */
    0x81, 0xCF,  /* contrast                       */
    0xD9, 0xF1,  /* pre-charge period              */
    0xDB, 0x40,  /* VCOMH deselect                 */
    0xA4,        /* resume from RAM                */
    0xA6,        /* normal (non-inverse)           */
    0xAF,        /* display on                     */
};

static void cmd(uint8_t c) {
    uint8_t buf[2] = { 0x00, c };
    i2c1_write(OLED_ADDR, buf, 2);
}

static void flush_pages(int page_start, int page_end) {
    /* Set column range 0..127. Each of these is a single command byte
     * preceded by the 0x00 control byte, hence one call each. */
    cmd(0x21); cmd(0x00); cmd(0x7F);
    cmd(0x22); cmd((uint8_t)page_start); cmd((uint8_t)page_end);

    static const uint8_t data_ctrl = 0x40;
    size_t n = (size_t)(page_end - page_start + 1) * OLED_W;
    i2c1_write2(OLED_ADDR, &data_ctrl, 1, &fb[page_start * OLED_W], n);
}

void oled_init(void) {
    i2c1_init();
    for (size_t i = 0; i < sizeof(OLED_INIT_SEQ); i++) cmd(OLED_INIT_SEQ[i]);
    oled_clear();
    flush_pages(0, OLED_PAGES - 1);
}

void oled_clear(void) {
    memset(fb, 0, sizeof(fb));
    cursor_row = 0;
    cursor_col = 0;
    flush_pages(0, OLED_PAGES - 1);
}

static void draw_char_at(int row, int col, char c) {
    int idx = (unsigned char)c - 0x20;
    if (idx < 0 || idx >= 95) idx = ('?' - 0x20);
    int x = col * CHAR_W;
    for (int i = 0; i < 5; i++) {
        fb[row * OLED_W + x + i] = font5x7[idx][i];
    }
    fb[row * OLED_W + x + 5] = 0;  /* 1-pixel spacer column */
}

static void scroll_up_one(void) {
    memmove(fb, fb + OLED_W, OLED_W * (OLED_PAGES - 1));
    memset(fb + OLED_W * (OLED_PAGES - 1), 0, OLED_W);
}

void oled_putc(char c) {
    int drew = 0;
    int scrolled = 0;

    if (c == '\n') {
        cursor_col = 0;
        cursor_row++;
    } else if (c == '\r') {
        cursor_col = 0;
    } else if ((unsigned char)c >= 0x20 && (unsigned char)c < 0x7F) {
        draw_char_at(cursor_row, cursor_col, c);
        cursor_col++;
        drew = 1;
    } else {
        return; /* non-printable: skip */
    }

    if (cursor_col >= COLS) {
        cursor_col = 0;
        cursor_row++;
    }
    if (cursor_row >= ROWS) {
        scroll_up_one();
        cursor_row = ROWS - 1;
        scrolled = 1;
    }

    /* Flush just what changed: a full frame after scroll, else one page. */
    if (scrolled) {
        flush_pages(0, OLED_PAGES - 1);
    } else if (drew) {
        flush_pages(cursor_row, cursor_row);
    }
}

void oled_puts(const char *s) {
    while (*s) oled_putc(*s++);
}
